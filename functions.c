/*
Function: block of code which we can use as many time as we need when we call it

Types:

1. User-defiened : main() . it has a body
2. built-in/predefined : printf(),scanf(),strlen(),strcat()

Function without parameters/args
Syntax :
datatype function-name()
{
	... code;
}
int main()
{
	function-name(); // calling to function
	
	return 0;
}


Function with parameters/args

Syntax :
datatype function-name(arg1,arg2,....,argN)
{
	... code;
}
int main()
{
	function-name(arg1,arg2,....,argN); // calling to function
	
	return 0;
}

*/